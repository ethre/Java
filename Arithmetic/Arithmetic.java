class Arithmetic {
	public static void main (String args[]) {

	int num = 100;
	int factor = 20;
	int sum = 0;


	sum = num + factor;
	System.out.println("Addition sum: " + sum);

	sum = num - factor;
	System.out.println("Subtraction sum: " + sum);

	sum = num * factor;
	System.out.println("Multiplicaton sum: " + sum);

	sum = num / factor;
	System.out.println("Division sum: " + sum);

	}

}